import com.example.AnnyCases.databinding.FragmentDescuentoBinding
import com.example.AnnyCases.databinding.FragmentSomosBinding


class SomosFragment : Fragment(R.layout.fragment_somos) {

    private var _binding: FragmentSomosBinding? = null

    // This property is only valid between onCreateView and
    // onDestroyView.
    private val binding get() = _binding!!

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View {

        _binding = FragmentSomosBinding.inflate(inflater, container, false)
        val root: View = binding.root


        return root
    }

    override fun onDestroyView() {
        super.onDestroyView()
        _binding = null
    }
}